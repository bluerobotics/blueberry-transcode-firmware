//*******************************************************************************************
//Defines
//*******************************************************************************************
#define PACKET_PREAMBLE (0x45554c42)
#define PACKET_LENGTH_INDEX (4)
#define PACKET_CRC_INDEX (6)
#define PACKET_PREAMBLE_INDEX (0)
#define PACKET_FIRST_MESSAGE_INDEX (8)
